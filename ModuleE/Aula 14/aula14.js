// usando while
let c = 1
while (c <= 5) {
  console.log(c)
  c++
}

// usando do/while
let cont = 1
do {
  console.log(cont);
  cont++;
} while (cont <= 5);

// usando for e testando o depurador
console.log('Vai começar...');
for (let i = 1; i <= 5; i++) {
  console.log(i);
}
console.log('Terminou!');
// for é o mais usado de todos